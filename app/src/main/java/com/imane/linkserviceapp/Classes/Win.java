package com.imane.linkserviceapp.Classes;

public class Win {
    private int id_badge;
    private int id_user;

    public Win(int id_badge, int id_user) {
        this.id_badge = id_badge;
        this.id_user = id_user;
    }

    public int getId_badge() {
        return id_badge;
    }

    public int getId_user() {
        return id_user;
    }
}
